# tipe data integer
main =  322
print("data : ",main)
print("tipe data main",type(main))
# tipe data float
main = 4.5
print("data : ",main)
print("tipe data",type(main))
# tipe data string
main = "string"
print("data : ",main)
print("tipe data",type(main))
# tipe data boolean true dan false
main = True
print("data : ",main)
print("tipe data",type(main))

# ! tipe data bilangan khusus

# tipe data bilangan kompleks
main = complex(4,5)
print("data : ",main)
print("tipe data",type(main))

# menggunakan tipe data c

from ctypes import c_double
main = c_double
print("data : ",main)
print("tipe data",type(main))
